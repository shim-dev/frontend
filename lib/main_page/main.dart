        } else {
          double sugarRatio = sugarPerG / carbPerG;
          if (sugarRatio <= 0.15) {
            carbScore = 10;
          } else if (sugarRatio <= 0.3) {
            carbScore = 5;
          } else {
            carbScore = 0;
          }
        }

        // ✅ 지방 보너스 (농도 기준)
        double fatBonus;
        if (fat <= 5) {
          fatBonus = 10;
        } else if (fat <= 15) {
          fatBonus = 5;
        } else {
          fatBonus = 0;
        }

        double totalScore =
            energyScore +
            proteinScore +
            sodiumScore +
            sugarScore +
            satFatScore +
            carbScore +
            fatBonus;

        snScore += totalScore.clamp(0, 100).round();
        sn_itemCount++;
      }
    }
    snackScore.value = sn_itemCount == 0 ? 0 : (snScore / sn_itemCount).round();
  }

